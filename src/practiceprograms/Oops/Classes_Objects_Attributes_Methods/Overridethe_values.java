package practiceprograms.Oops.Classes_Objects_Attributes_Methods;

public class Overridethe_values {

    int var1 = 25;


    public static void main(String[] args) {

        Overridethe_values override = new Overridethe_values();
        override.var1 = 90;  // var 1 is now 90
        System.out.println(override.var1);

    }
}


