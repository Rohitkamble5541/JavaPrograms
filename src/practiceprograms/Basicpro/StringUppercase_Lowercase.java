package practiceprograms.Basicpro;

public class StringUppercase_Lowercase {

    public static void main(String[] args) {
        String name = "Rohit Abaso kambel";

        // converting string into the uppercase
        System.out.println("String convert into to uppercase :-" + name.toUpperCase());

        // converting string into the lowercase
        System.out.println("String convert into to uppercase :-" + name.toLowerCase());
    }
}
