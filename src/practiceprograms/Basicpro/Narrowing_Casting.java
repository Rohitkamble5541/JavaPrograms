package practiceprograms.Basicpro;

public class Narrowing_Casting {

    public static void main(String[] args) {

        // converting big type container values to small type container i.e. called Narrowing casting
        // Narrowing Casting (manually) - converting a larger type to a smaller size type

        double value1 = 9.78d;
        int value2 =(int)value1; // Manual casting: double to int
        System.out.println(value1);
        System.out.println(value2);

    }
}
