package practiceprograms.Loops;

public class Whileloop {


    /* syntax of while loop
    while (condition) {
  // code block to be executed
                        }

     */

    public static void main(String[] args) {

        int i =0;
        while (i <= 5)
        {
            System.out.println(i);
            i++;
        }
    }


}
