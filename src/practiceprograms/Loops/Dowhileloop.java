package practiceprograms.Loops;

public class Dowhileloop {


    /*
    Syntax
do {
  // code block to be executed
}
while (condition);
     */

    public static void main(String[] args) {

        // print out 0 to 5 numbers using do while loop
        int i =0;

        do {
            System.out.println(i);
           i++;

        }while (i<=5);

    }
}
